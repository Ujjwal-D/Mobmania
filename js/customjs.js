// Function to load carousel images, YouTube video, phone name, key features, specifications, price, and seller info
function loadProductDetails(productId) {
    const product = productData[productId];
    if (product) {
        // Update Phone Name
        document.getElementById('phoneName').textContent = product.name;

        // Update Carousel Images
        document.getElementById('carouselImage1').src = product.images.front;
        document.getElementById('carouselImage2').src = product.images.right;
        document.getElementById('carouselImage3').src = product.images.back;
        document.getElementById('carouselImage4').src = product.images.left;

        // Load YouTube Video
        document.getElementById('youtubeVideo').src = product.video;

        // Update Key Features
        const keyFeaturesList = document.getElementById('keyFeaturesList');
        keyFeaturesList.innerHTML = product.keyFeatures.map(feature => `<li>${feature}</li>`).join('');

        // Update Specifications
        const specificationsList = document.getElementById('specificationsList');
        specificationsList.innerHTML = Object.entries(product.specifications)
            .map(([key, value]) => `<li>${key}: ${value}</li>`).join('');

        // Update Price and Seller
        document.getElementById('productPrice').textContent = product.price;
        document.getElementById('sellerName').textContent = product.seller;
    } else {
        console.error("Product data not found for ID:", productId);
    }
}

// Get product ID from the URL
const urlParams = new URLSearchParams(window.location.search);
const productId = urlParams.get('id');

// Load the product data if a valid ID is provided
if (productId) {
    loadProductDetails(productId);
} else {
    console.error("Product ID not found in URL.");
}
